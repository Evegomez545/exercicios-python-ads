a=input('Digite algo:')
print('O tipo primitivo desse valor é',type(a))
print('Só tem espeços?',a.isspace())
print('É um número?',a.isnumeric())
print('É alfabetico?',a.isalnum())
print('É alfanúmerico?',a.isalnum())
print('Está em maiuscula?',a.isalnum())
print('Está em munúscula?',a.islower())
print('Está capilarizada?',a.istitle())




